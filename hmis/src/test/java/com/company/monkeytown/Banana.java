package com.company.monkeytown;

public class Banana {
    private int a;
    private String b;

    public Banana(int a, String b) {
        this.a = a;
        this.b = b;
    }


    /**
     * @return int
     */
    public int getA() {
        return a;
    }


    /**
     * @return String
     */
    public String getB() {
        return b;
    }
}
